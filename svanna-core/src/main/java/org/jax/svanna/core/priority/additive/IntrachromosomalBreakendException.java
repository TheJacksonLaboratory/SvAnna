package org.jax.svanna.core.priority.additive;

public class IntrachromosomalBreakendException extends DispatchException {

    public IntrachromosomalBreakendException() {
        super();
    }

    public IntrachromosomalBreakendException(String message) {
        super(message);
    }

    public IntrachromosomalBreakendException(String message, Throwable cause) {
        super(message, cause);
    }

    public IntrachromosomalBreakendException(Throwable cause) {
        super(cause);
    }

    protected IntrachromosomalBreakendException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
